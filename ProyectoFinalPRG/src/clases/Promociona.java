package clases;

public class Promociona {

	private int idPatro;
	private int idObra;

	public Promociona() {
		super();
	}

	public Promociona(int idPatro, int idObra) {
		super();
		this.idPatro = idPatro;
		this.idObra = idObra;
	}

	public int getIdPatro() {
		return idPatro;
	}

	public void setIdPatro(int idPatro) {
		this.idPatro = idPatro;
	}

	public int getIdObra() {
		return idObra;
	}

	public void setIdObra(int idObra) {
		this.idObra = idObra;
	}

}
