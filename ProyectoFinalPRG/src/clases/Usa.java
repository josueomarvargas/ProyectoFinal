package clases;

public class Usa {

	private int idObra;
	private int idEquip;

	public Usa() {
		super();
	}

	public Usa(int idObra, int idEquip) {
		super();
		this.idObra = idObra;
		this.idEquip = idEquip;
	}

	public int getIdObra() {
		return idObra;
	}

	public void setIdObra(int idObra) {
		this.idObra = idObra;
	}

	public int getIdEquip() {
		return idEquip;
	}

	public void setIdEquip(int idEquip) {
		this.idEquip = idEquip;
	}

}
